"""
булевы значения True и False
операторы сравнения ==, >, <, >=, <=, !=;
условный оператор if-elif-else;
логические операторы and, or, not и in.
"""

floor = 1

print('Значение floor больше 0:', floor > 0)
print('Значение floor больше 1:', floor > 1)
print()

print('Значение floor меньше 2:', floor < 2)
print('Значение floor меньше 1:', floor < 1)
print()

print('Значение floor больше или равно 1:', floor >= 1)
print('Значение floor больше или равно 2:', floor >= 2)
print()

print('Значение floor меньше или равно 1:', floor <= 1)
print('Значение floor меньше или равно 0:', floor <= 0)
print()

print('Значение floor не равно 0:', floor != 0)
print('Значение floor не равно 1:', floor != 1)